"""
–°–ê–ú–ê–Ø –ü–†–û–°–¢–ê–Ø –ò–ì–†–ê –° print() –ò input()
======================================

–ò–≥—Ä–∞ ¬´–£–≥–∞–¥–∞–π —Å–ª–æ–≤–æ¬ª.
–ö–æ–º–ø—å—é—Ç–µ—Ä –∑–∞–≥–∞–¥–∞–ª –æ–¥–Ω–æ –∏–∑ —Å–ª–æ–≤: –ö–û–¢, –ü–Å–° –∏–ª–∏ –°–û–í–ê.
–†–µ–±—ë–Ω–æ–∫ –≤–≤–æ–¥–∏—Ç –æ—Ç–≤–µ—Ç —á–µ—Ä–µ–∑ input(), –∞ –ø—Ä–æ–≥—Ä–∞–º–º–∞ —Å –ø–æ–º–æ—â—å—é if –ø—Ä–æ–≤–µ—Ä—è–µ—Ç
–ø—Ä–∞–≤–∏–ª—å–Ω–æ—Å—Ç—å –∏ –¥–∞—ë—Ç –ø–æ–¥—Å–∫–∞–∑–∫—É. –¢–æ–ª—å–∫–æ –±–∞–∑–æ–≤—ã–µ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ Python!
"""

import random

# –°–ø–∏—Å–æ–∫ —Å–ª–æ–≤, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–µ—Ç –∑–∞–≥–∞–¥–∞—Ç—å –∫–æ–º–ø—å—é—Ç–µ—Ä.
# random.choice(...) –≤—ã–±–µ—Ä–µ—Ç –æ–¥–Ω–æ —Å–ª—É—á–∞–π–Ω–æ–µ —Å–ª–æ–≤–æ –∏–∑ —Å–ø–∏—Å–∫–∞.
WORDS = ["–∫–æ—Ç", "–ø—ë—Å", "—Å–æ–≤–∞"]
secret_word = random.choice(WORDS)

print("=" * 40)
print("üêæ –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ –∏–≥—Ä—É ¬´–£–≥–∞–¥–∞–π —Å–ª–æ–≤–æ¬ª!")
print("–Ø –∑–∞–≥–∞–¥–∞–ª –æ–¥–Ω–æ –∏–∑ —Å–ª–æ–≤: –∫–æ—Ç, –ø—ë—Å –∏–ª–∏ —Å–æ–≤–∞.")
print("–ü–æ–ø—Ä–æ–±—É–π —É–≥–∞–¥–∞—Ç—å. –£ —Ç–µ–±—è –≤—Å–µ–≥–æ 3 –ø–æ–ø—ã—Ç–∫–∏!")
print("=" * 40)

# –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Ç—Ä—ë–º –ø–æ–ø—ã—Ç–∫–∞–º —Å –ø–æ–º–æ—â—å—é —Ü–∏–∫–ª–∞ for.
for attempt in range(1, 4):
    # input() ‚Äî —Å–ø—Ä–∞—à–∏–≤–∞–µ–º —Å–ª–æ–≤–æ —É —Ä–µ–±—ë–Ω–∫–∞.
    guess = input(f"–ü–æ–ø—ã—Ç–∫–∞ {attempt}: ").strip().lower()

    if guess == secret_word:
        print("üéâ –ü—Ä–∞–≤–∏–ª—å–Ω–æ! –¢—ã —É–≥–∞–¥–∞–ª —Å–ª–æ–≤–æ!")
        break  # –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞, –∏–≥—Ä–∞ –æ–∫–æ–Ω—á–µ–Ω–∞
    else:
        print("–ù–µ—Ç, —ç—Ç–æ –Ω–µ —Ç–æ —Å–ª–æ–≤–æ.")
        # –ü—Ä–æ—Å—Ç–∞—è –ø–æ–¥—Å–∫–∞–∑–∫–∞: —Å–æ–æ–±—â–∏–º –ø–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ –∏–∑ —Å–ø–∏—Å–∫–∞,
        # –µ—Å–ª–∏ —Ä–µ–±—ë–Ω–æ–∫ —Å–æ–≤—Å–µ–º –æ—à–∏–±—Å—è (–Ω–µ –∏–∑ —Ç—Ä—ë—Ö –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤).
        if guess not in WORDS:
            print("–ü–æ–¥—Å–∫–∞–∑–∫–∞: –≤—ã–±–µ—Ä–∏ –æ–¥–Ω–æ –∏–∑ —Å–ª–æ–≤ ‚Äî –∫–æ—Ç, –ø—ë—Å –∏–ª–∏ —Å–æ–≤–∞.")
        elif guess < secret_word:
            print("–ü–æ–¥—Å–∫–∞–∑–∫–∞: –º–æ—ë —Å–ª–æ–≤–æ –±–ª–∏–∂–µ –∫ –∫–æ–Ω—Ü—É –∞–ª—Ñ–∞–≤–∏—Ç–∞.")
        else:
            print("–ü–æ–¥—Å–∫–∞–∑–∫–∞: –º–æ—ë —Å–ª–æ–≤–æ –±–ª–∏–∂–µ –∫ –Ω–∞—á–∞–ª—É –∞–ª—Ñ–∞–≤–∏—Ç–∞.")
        print()
else:
    # –ï—Å–ª–∏ —Ü–∏–∫–ª –∑–∞–∫–æ–Ω—á–∏–ª—Å—è –±–µ–∑ break, –∑–Ω–∞—á–∏—Ç –ø–æ–ø—ã—Ç–∫–∏ –∏—Å—á–µ—Ä–ø–∞–Ω—ã.
    print("üò¢ –ü–æ–ø—ã—Ç–∫–∏ –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å. –ú–æ—ë —Å–ª–æ–≤–æ –±—ã–ª–æ:", secret_word)

print("–°–ø–∞—Å–∏–±–æ –∑–∞ –∏–≥—Ä—É! –ó–∞–ø—É—Å—Ç–∏ —Ñ–∞–π–ª —Å–Ω–æ–≤–∞, —á—Ç–æ–±—ã —Å—ã–≥—Ä–∞—Ç—å –µ—â—ë —Ä–∞–∑.")

