"""
Простой калькулятор на Python
==============================

Этот калькулятор выполняет основные математические операции:
- Сложение (+)
- Вычитание (-)
- Умножение (*)
- Деление (/)
"""


def to_number(text):
    """
    Превращает текст в число.
    Пример: "2,5" превратится в 2.5, "3" превратится в 3.
    """
    try:
        # Заменяем запятую на точку (в Python числа пишутся с точкой)
        text = text.replace(',', '.')
        # Превращаем текст в число
        return float(text)
    except ValueError:
        # Если не получилось превратить в число, возвращаем None
        return None


def calculate(a_text, operation, b_text):
    """
    Выполняет математическое вычисление.
    
    Параметры:
        a_text - первое число в виде текста
        operation - знак операции (+, -, *, /)
        b_text - второе число в виде текста
    """
    # Превращаем оба текста в числа
    a = to_number(a_text)
    b = to_number(b_text)
    
    # Проверяем, что оба числа получилось превратить
    if a is None or b is None:
        return "Ошибка: введите числа (например: 2 или 4.5)"
    
    # Выполняем нужное действие в зависимости от знака
    if operation == '+':
        return a + b  # Сложение
    elif operation == '-':
        return a - b  # Вычитание
    elif operation == '*':
        return a * b  # Умножение
    elif operation == '/':
        # Проверяем деление на ноль
        if b == 0:
            return "Ошибка: нельзя делить на ноль!"
        return a / b  # Деление
    else:
        return "Ошибка: неизвестная операция. Используйте +, -, *, /"


def main():
    """Главная функция - здесь происходит основная работа программы."""
    print("=" * 50)
    print("   ПРОСТОЙ КАЛЬКУЛЯТОР")
    print("=" * 50)
    print()
    
    # Шаг 1: Ввод первого числа
    print("Шаг 1: Введите первое число")
    a_text = input("   Первое число: ").strip()
    
    # Шаг 2: Выбор операции
    print()
    print("Шаг 2: Выберите знак")
    print("   Доступные операции: +, -, *, /")
    operation = input("   Операция: ").strip()
    
    # Шаг 3: Ввод второго числа
    print()
    print("Шаг 3: Введите второе число")
    b_text = input("   Второе число: ").strip()
    
    print()
    print("-" * 50)
    print()
    
    # Шаг 4: Выполняем вычисление и показываем результат
    result = calculate(a_text, operation, b_text)
    
    print("РЕЗУЛЬТАТ:")
    print(f"   {a_text} {operation} {b_text} = {result}")
    print()
    print("=" * 50)


# Запуск программы
if __name__ == "__main__":
    main()

